#include<stdio.h>
int main()
{
    int i;
    int j;
    int mark[2][3] =
    {
        {1,2,3},
        {7,5,4}
    };
    for(i = 0; i < 2; i++)
    {
        for(j = 0; j < 3; j++)
        {
            printf("%d ",mark[i][j]);
        }
        printf("\n");
    }
}
