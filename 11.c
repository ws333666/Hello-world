#include<stdio.h>
int main()
{
    int n,x,a = 0;
    scanf("%d %d",&n,&x);
    for(int i = 0;i <= n; i++)
    {
        if(i % 10 == x||i / 10 == x)
        {
            a++;
        }
    }
    printf("%d",a);
    return 0;
}
