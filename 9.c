#include<stdio.h>
int main()
{
    int n;
    scanf("%d",&n);
    if(n <= 240)
    {
        printf("%.1f",0.4783 * n);
    }
    else if(n >= 241 && n <= 400)
    {
        printf("%.1f",0.4783 * 240 + 0.5283 * (n - 240));
    }
    else if(n >= 401)
    {
        printf(".1f",0.4783 * 240 + 0.5283 * 160 + 0.7783 * (n -400));
    }
    return 0;
}
