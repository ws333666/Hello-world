#include<stdio.h>
int main()
{
    int t;
    scanf("%d",&t);
    if(t < 43200)
    {
        printf("%02d:%02d:%02d am",t / 3600,(t % 3600) / 60,t % 60);
    }
    else if(t >= 43200 && t <= 46800)
    {
        printf("12:%02d:%02d midnoon",(t - 43200) / 60,(t - 43200) % 60);
    }
    else if(t > 46800)
    {
        printf("%02d:%02d:%02d pm",(t - 43200) / 3600,((t -43200) % 3600) /60,(t -43200 % 60)) ;  
    }
    return 0;
}

