#include<stdio.h>
int main()
{
    int n;
    char a = 'A';
    scanf("%d",&n);
    for(int i = 0;i < n;i ++)
    {
        for(int j = 0;j <(n - i) * 2 ;j ++)
        {
            printf("%c",a);
        }
        printf("\n");
    }
}
